    private void crear() {
        Integer tamanio = Integer.parseInt(cbxtamanio.getSelectedItem().toString());
        cc = new ComandoController(tamanio);
    }

    private void guardar() {
        if (cc.getCola().estaLleno()) {
            cc.extraer(0);
            cc.insertarc(txtcomando.getText());
        } else {
            cc.insertarc(txtcomando.getText());
        }
        cargarTabla();

        limpiar();
    }

    private void cargarTabla() {
        try {
            ct.setLista(cc.getCola());
            tbltabla.setModel(ct);
            tbltabla.updateUI();
        } catch (Exception e) {
        }

    }

    private void limpiar() {
        txtcomando.setText("");
    }

